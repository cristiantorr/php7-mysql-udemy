        <h1>OPERADORES</h1>
        <ul>
            <li>Un operador es algo que toma uno o más valores (literales, variables u otros operadores) y produce otro valor (de modo que la construcción en si misma se convierte en una expresión)</li>
            <li>Los operadores se pueden agrupar de acuerdo con el número de valores que toman. los operadores unarios toman sñolo un valor, por ejemplo: !(el operador lógico de negación) o ++ (el operador de incremento).</li>
            <li>Los operadores binarios toman dos valores, como los familiares operadores aritméticos + (suma) y - (resta), y la mayoría de los operadores de PHP entran en esta categoría.</li>
            <li>Finalmente, hay sólo un operador ternario, ?:, el cual toma tres valores; usualmente a este se le refiere simplemente como "el operador ternario" (aunque podría llamarse operador condicional).</li>
            <li><strong>CLASES DE OPERADORES: </strong>
                <ul>
                    <li>Operadores aritméticos</li>
                    <li>Operadores de asignación</li>
                    <li>Operadores bit a bit</li>
                    <li>Operadores de comparación</li>
                    <li>Operadores de ejecución</li>
                    <li>Operadores de incremento/decremento</li>
                    <li>Operadores Lógicos</li>
                    <li>Operadores para cadenas</li>
                    <li>Operadores para arreglos</li>
                    <li>Operadores de tipo</li>
                </ul>
            </li>
            <li>La precedencia de un operador indica la prioridad en que se ejecutan los operadores. Por ejemplo, en la expresión 1 + 5 * 3, la respuesta es 16 y no 18 por que el operador de multiplicación("*") tiene una precedencia mayor que el operador de adición("+").</li>
            <li>Los paréntisis pueden ser usuados para forzar la precedencia, si es necesario. Por ejemplo: (1 + 5) * 3 se evalúa como 18.</li>
            <li><strong>LAS EXPRESIONES:</strong>
                <ul>
                    <li>Una expresión es la combinación de literales , operadores, variables y funciones, bajo la sintaxis correcta del lenguaje, que regresan un valor válido para PHP.</li>
                </ul>
            </li>
            <li><strong>lITERALES:</strong>
                <ul>
                    <li>Valores que no cambian, como número (66) o cadenas ("Hola mundo") 0 valores booleanos (true)</li>
                </ul>
            </li>
        </ul>
