        <h1>Niklaus Wirth:<small> (Winterthur Suiza, 15 de febrero de 1934), científico de la computación.</small></h1>

        <p>Wirth fue el jefe de diseño de los lenguajes de programación Euler, Algol W, Pascal, Modula, Modula-2 y Oberon. También ocupó gran parte de su tiempo en el equipo de diseño e implementación de sistemas operativos Lilith y Oberon para el Lola en el diseño del hardware digital y el sistema de simulación.</p>
        <p>Su artículo de desarrollo de un programa por refinamiento sucesivo ("program development by stepwise refinement") se considera un texto clásico en la ingeniería del software, así como su libro Algoritmos + Estructuras de datos = Programas, que recibió un amplio reconocimiento, y que aún hoy resulta útil en la enseñanza de la programación. Recibió el Premio Turing por el desarrollo de estos lenguajes de programación en 1984. Se jubiló en 1999.</p>
